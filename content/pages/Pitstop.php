  <h2>O que é?</h2>
  <br>

  <p>Na Fórmula 1, não só o desempenho dos carros na pista determinara quem vence a corrida. Muitas vezes, o pit stop
    é o fator decisivo para a chegada ao pódio.</p>

  <p>Ao longo da história da Fórmula 1, as regras de pit stop mudaram. Desde 2010, por questões de segurança,
    reabastecimento de combustível já não é mais permitido. Agora, os pilotos apenas trocam pneus.</p>
  
    <p>Essa mudança das regras levou as equipas a prepararem-se cada vez melhor para conseguir paragens rápidas nas
    boxes, pois este é um momento que pode ser decisivo.</p>
  <hr>
  
  <h2>Quantas pessoas estão envolvidas num Pitstop?</h2>
  <br>
  <p>São vários os intervenientes necessários para <b>mudar 4 pneus em cerca de 3 segundos!</b></p>

  <p>Estão envolvidas cerca de 20 pessoas num pit stop. São três mecânicos para a troca de cada um dos 4 pneus — um
    com a pistola de ar comprimido para desenroscar a porca, outro para retirar o pneu usado e o terceiro para colocar
    o novo.</p>
  
    <p>Existem dois mecânicos que se encarregam de levantar o carro com macacos na parte dianteira e traseira. Há ainda
    um elemento que controla o tráfego do <i>pitlane</i> e um elemento com um extintor de incêndio.</p>
  <hr>
  
  <h2>A quem pertence o Pit Stop mais rápido?</h2>
  <br>
  <p>O recorde de pit stop mais rápido da F1 pertence a <b>Max Verstappen</b> e à equipa <b> Red Bull</b> com um tempo de <b>1,82 segundos</b>. Este feito foi conseguido no Autódromo de Interlagos, em São Paulo, no Grande Prémio do Brasil, a 17 de novembro de 2019.</p>
